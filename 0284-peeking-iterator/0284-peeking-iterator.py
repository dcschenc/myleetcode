class PeekingIterator:
    def __init__(self, iterator):
        self.iterator = iterator
        self._next = iterator.next()        

    def peek(self):
        return self._next        

    def next(self):
        # if self.next is None:
        #     raise StopIteration()
        data = self._next
        self._next = None
        if self.iterator.hasNext():
            self._next = self.iterator.next()
        return data        

    def hasNext(self):
        return self._next is not None
        

